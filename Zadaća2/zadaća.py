import math

class Krug:
    def __init__(self, r):
        self.r = r

    def opseg(self):
        return 2 * math.pi * self.r

    def povrsina(self):
        return math.pi * (self.r ** 2)


k = Krug(5)

print("Opseg:", k.opseg())
print("Površina:", k.povrsina())

class Radnik:
    def __init__(self, ime, pozicija, placa):
        self.ime = ime
        self.pozicija = pozicija
        self.placa = placa

    def work(self):
        print(f"Radim na poziciji {self.pozicija}")

class Manager(Radnik):
    def __init__(self, ime, pozicija, placa, department):
        super().__init__(ime, pozicija, placa)
        self.department = department

    def work(self):
        print(f"Radim na poziciji {self.pozicija} u odjelu {self.department}")

    def give_raise(self, radnik, povecanje):
        radnik.placa += povecanje
        print(f"{radnik.ime} sada ima plaću {radnik.placa}.")

drzavni_sluzbenik = Radnik("Damir Kajin", "Gradonacelnik Buzeta", 8000)
drzavni_sluzbenik_zesci = Manager("Andrej Plenki", "Premijer", 12000, "Prodavač magle")

drzavni_sluzbenik.work()
drzavni_sluzbenik_zesci.work()

# Sam sebi je diga plaću
drzavni_sluzbenik_zesci.give_raise(drzavni_sluzbenik_zesci, 2000)
