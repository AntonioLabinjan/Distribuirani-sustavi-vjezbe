from datetime import datetime

class Automobil:
    def __init__(self, marka, model, godina_proizvodnje, kilometraza):
        self.marka = marka
        self.model = model
        self.godina_proizvodnje = godina_proizvodnje
        self.kilometraza = kilometraza

    def ispis(self):
        print(f"Marka: {self.marka}")
        print(f"Model: {self.model}")
        print(f"Godina proizvodnje: {self.godina_proizvodnje}")
        print(f"Kilometraža: {self.kilometraza} km")

    def starost(self):
        trenutna_godina = datetime.now().year
        star = trenutna_godina - self.godina_proizvodnje
        print(f"Automobil je star {star} lit.")


auto = Automobil("Yugo", "45", 1968, 450000)

auto.ispis()
auto.starost()

import math

class Kalkulator:
    def __init__(self, a, b):
        self.a = a
        self.b = b

    def zbroj(self):
        return self.a + self.b

    def oduzimanje(self):
        return self.a - self.b

    def mnozenje(self):
        return self.a * self.b

    def dijeljenje(self):
        if self.b == 0:
            return "Ne moreš dijelit z nulon trubilo."
        return self.a / self.b

    def potenciranje(self):
        return self.a ** self.b

    def korijen(self):
        if self.a < 0:
            return "Ne moreš korijenovat negativan broj trubilo."
        return math.sqrt(self.a)


k = Kalkulator(9, 0)

print(k.zbroj())
print(k.oduzimanje())
print(k.mnozenje())
print(k.dijeljenje())
print(k.potenciranje())
print(k.korijen())

class Student:
    def __init__(self, ime, prezime, godine, ocjene):
        self.ime = ime
        self.prezime = prezime
        self.godine = godine
        self.ocjene = ocjene

    def prosjek(self):
        return sum(self.ocjene) / len(self.ocjene)


